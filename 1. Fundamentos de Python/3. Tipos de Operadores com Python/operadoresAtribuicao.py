print('< OPERADORES DE ATRIBUIÇÃO >')
# - Definem o valor inicial ou subrescrevem (alteram) o valor de uma variável

# ATRIBUIÇÃO SIMPLES (=)
saldo = 500 # O valor 500 está sendo ATRIBUÍDO à variável saldo
print('ATRIBUIÇÃO:', saldo, '\n')

# ATRIBUIÇÃO COM ADIÇÃO (+=)
saldo += 200 # saldo = saldo + 200
print('> C/ADIÇÃO:', saldo)

# ATRIBUIÇÃO COM SUBTRAÇÃO (-=)
saldo -= 100 # saldo = saldo - 100
print('> C/SUBTRAÇÃO:', saldo)

# ATRIBUIÇÃO COM MULTIPLICAÇÃO (*=)
saldo *= 2 # saldo = saldo * 2
print('> C/MULTIPLICAÇÃO:', saldo)

# ATRIBUIÇÃO COM DIVISÃO (/=)
saldo /= 5 # saldo = saldo / 5
print('> C/DIVISÃO:', saldo)

# ATRIBUIÇÃO COM DIVISÃO EXATA (//=)
saldo //= 5 # saldo = saldo // 5
print('> C/DIVISÃO EXATA:', saldo)

# ATRIBUIÇÃO COM MÓDULO (%=)
saldo %= 5 # saldo = saldo % 5
print('> C/MÓDULO:', saldo)

# ATRIBUIÇÃO COM EXPONENCIAÇÃO (**=)
saldo **= 6 # saldo = saldo ** 6
print('> C/EXPONENCIAÇÃO:', saldo)
